# 3. ‡∏™‡πà‡∏ß‡∏ô‡∏Å‡∏≤‡∏£‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡πÅ‡∏•‡∏∞‡πÄ‡∏ï‡∏£‡∏µ‡∏¢‡∏°‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
@st.cache_data # ‡∏ä‡πà‡∏ß‡∏¢‡πÉ‡∏´‡πâ‡πÇ‡∏´‡∏•‡∏î‡πÄ‡∏£‡πá‡∏ß‡∏Ç‡∏∂‡πâ‡∏ô‡πÄ‡∏´‡∏°‡∏∑‡∏≠‡∏ô‡∏£‡∏∞‡∏ö‡∏ö‡∏°‡∏∑‡∏≠‡∏≠‡∏≤‡∏ä‡∏µ‡∏û
def load_data():
    sample_size = 1500
    df = pd.DataFrame({
        'HN': [f'{i:06d}' for i in range(1, sample_size + 1)],
        '‡∏ä‡∏∑‡πà‡∏≠-‡∏™‡∏Å‡∏∏‡∏•': [f'‡∏Ñ‡∏ô‡πÑ‡∏Ç‡πâ ‡∏à‡∏≥‡∏•‡∏≠‡∏á {i}' for i in range(1, sample_size + 1)],
        'BPS': np.random.randint(90, 190, sample_size),
        'BPD': np.random.randint(60, 110, sample_size),
        '‡∏≠‡∏≤‡∏¢‡∏∏': np.random.randint(15, 90, sample_size),
        '‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡πå‡∏Å‡∏≤‡∏£‡∏£‡∏±‡∏Å‡∏©‡∏≤': np.random.choice(['‡∏ä‡∏≥‡∏£‡∏∞‡πÄ‡∏á‡∏¥‡∏ô‡πÄ‡∏≠‡∏á', '‡∏ö‡∏±‡∏ï‡∏£‡∏ó‡∏≠‡∏á', '‡∏õ‡∏£‡∏∞‡∏Å‡∏±‡∏ô‡∏™‡∏±‡∏á‡∏Ñ‡∏°', '‡∏Ç‡πâ‡∏≤‡∏£‡∏≤‡∏ä‡∏Å‡∏≤‡∏£'], sample_size)
    })
    return df

df = load_data()
filtered_df = df[df['BPS'] >= threshold].sort_values('BPS', ascending=False)

# 4. ‡∏™‡πà‡∏ß‡∏ô‡πÅ‡∏™‡∏î‡∏á‡∏ï‡∏±‡∏ß‡∏ä‡∏µ‡πâ‡∏ß‡∏±‡∏î (Key Performance Indicators - KPIs)
st.subheader("‡∏™‡∏£‡∏∏‡∏õ‡∏™‡∏ñ‡∏¥‡∏ï‡∏¥‡∏™‡∏≥‡∏Ñ‡∏±‡∏ç")
